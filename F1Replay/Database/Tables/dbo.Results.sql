CREATE TABLE [dbo].[results] (
  [resultId]		varchar(255)				NOT NULL,
  [raceId]			varchar(255)				NOT NULL	DEFAULT '0',
  [driverId]		varchar(255)				NOT NULL	DEFAULT '0',
  [constructorId]	varchar(255)				NOT NULL	DEFAULT '0',
  [number]			varchar(255)							DEFAULT NULL,
  [grid]			varchar(255)				NOT NULL	DEFAULT '0',
  [position]		varchar(255)							DEFAULT NULL,
  [positionText]	varchar(255)				NOT NULL	DEFAULT '',
  [positionOrder]	varchar(255)				NOT NULL	DEFAULT '0',
  [points]			varchar(255)				NOT NULL	DEFAULT '0',
  [laps]			varchar(255)				NOT NULL	DEFAULT '0',
  [time]			varchar(255)							DEFAULT NULL,
  [milliseconds]	varchar(255)							DEFAULT NULL,
  [fastestLap]		varchar(255)							DEFAULT NULL,
  [rank]			varchar(255)							DEFAULT '0',
  [fastestLapTime]	varchar(255)							DEFAULT NULL,
  [fastestLapSpeed]	varchar(255)							DEFAULT NULL,
  [statusId]		varchar(255)				NOT NULL	DEFAULT '0',
  PRIMARY KEY ([resultId]),
)
